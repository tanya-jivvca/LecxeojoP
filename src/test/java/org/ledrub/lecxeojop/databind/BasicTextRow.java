package org.ledrub.lecxeojop.databind;

public class BasicTextRow {
    private String colA;
    private String colB;
    private String colC;

    public String getColA() {
        return colA;
    }
    public void setColA(String colA) {
        this.colA = colA;
    }
    public String getColB() {
        return colB;
    }
    public void setColB(String colB) {
        this.colB = colB;
    }
    public String getColC() {
        return colC;
    }
    public void setColC(String colC) {
        this.colC = colC;
    }
}
